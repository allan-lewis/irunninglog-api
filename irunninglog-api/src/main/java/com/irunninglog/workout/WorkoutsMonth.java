package com.irunninglog.workout;

public final class WorkoutsMonth {

    private String title;
    private String date;

    public String getTitle() {
        return title;
    }

    public WorkoutsMonth setTitle(String title) {
        this.title = title;
        return this;
    }

    public String getDate() {
        return date;
    }

    public WorkoutsMonth setDate(String date) {
        this.date = date;
        return this;
    }

}
